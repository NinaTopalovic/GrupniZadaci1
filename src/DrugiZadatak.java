
public class DrugiZadatak {
	
	public int zbir(int a, int b) {
		int zbir=a+b;
		return zbir;
		
	}
	
	public int razlika(int a, int b) {
		int razlika=a-b;
		return razlika;
	}
	
	public int proizvod(int a, int b) {
		int proizvod=a*b;
		return proizvod;
	}
	
	public int kolicnik(int a, int b) {
		int kolicnik=a/b;
		return kolicnik;
	}
	
	public int kvadrat(int a) {
		int kvadrat=a*a;
		return kvadrat;
	}
	public int kvadratniKorijen(int a) {
		int kvadratniKorijen=(int)Math.sqrt(a);
		return kvadratniKorijen;
	}

}
